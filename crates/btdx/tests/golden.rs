fn build(format: Ba2Format) -> Vec<u8> {
    let mut w = GnrlWriter::new();
    w.format(format);
    w.add_file_stored("a.txt", b"hi".to_vec()).unwrap();
    w.to_vec().unwrap()
}

#[test]
fn v1_gnrl_matches_the_golden() {
    assert_eq!(build(Ba2Format::Fo4).as_slice(), GOLDEN_V1_GNRL);
}

#[test]
fn v2_gnrl_matches_the_golden() {
    assert_eq!(build(Ba2Format::StarfieldV2).as_slice(), GOLDEN_V2_GNRL);
}

#[test]
fn v3_gnrl_matches_the_golden() {
    assert_eq!(build(Ba2Format::StarfieldV3Lz4).as_slice(), GOLDEN_V3_GNRL);
}
